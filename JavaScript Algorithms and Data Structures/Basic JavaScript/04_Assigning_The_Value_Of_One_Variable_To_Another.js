
/**
 * ASSIGN THE VALUE OF ONE VARIABLE TO ANOTHER
 * >> BİR DEĞİŞKENİN DEĞERİNİ BİR BAŞKA DEĞİŞKENE ATAMA
 * 
 * After a value is assigned to a variable using the assignment operator, you can assign the value of that variable to another variable using the assignment operator.
 * >> Atama operatörü kullanılarak bir değişkene bir değer atandıktan sonra, atama operatörünü kullanarak o değişkenin değerini başka bir değişkene atayabiliriz.
 */

 var myVar;
 myVar = 5;
 
 var myNum;
 myNum = myVar;
 
 /**
  * The above declares a "myVar" variable with no value, than assigns it the value "5". Next, a variable named "myNum" is declared with no value. Then, the contents of "myVar" (which is "5") is assigned to the variable "myNum". Now, "myNum" also has the value of "5"
  * >> Yukarıda, değeri olmayan bir "myVar" değişkeni tanımlanır, ona "5" değerini atar. Ardından, "myNum" adlı bir değişken değeri olmayan bir değişken tanımlanır. Ardından, "myVar"ın ("5" olan) değeri "myNum" değişkenine atanır. Şimdi, "myNum" da "5" değerine sahip
  */
 
