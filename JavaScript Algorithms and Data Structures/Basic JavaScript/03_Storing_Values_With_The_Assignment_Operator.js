/**
 * STORING VALUES WITH THE ASSIGNMENT OPERATOR
 * >> DEĞERLERİ ATAMA OPERATÖRÜ İLE DEPOLAMA
 * 
 * In JavaScript, we can store a value in a variable with the assignment operator " = "
 * >> JavaScript'te, " = " atama operatörü ile bir değişkende bir değer saklayabiliriz.
 * 
 *  myVariable = 5;
 * 
 * This assigns the "Number" value "5" to "myVariable"
 * >> Bu, "5" değerini "myVariable" a atar.
 * 
 * If there are any calculations to the right of the " = " operator, those are performed before the value is assigned to the variable on the left of the operator.
 * >> " = " operatörünün sağ tarafında herhangi bir hesaplama varsa, bunlar operatörün solundaki değişkene değer atanmadan önce yapılır.
 */
 
var myVar;

myVar = 5;

/** 
 First, this code creates a variable named "myVar". Then, the code assigns "5" to "myVar". Now, if "myVar" appears again in the code, the program will treat it as if it is "5".
 * >> İlk olarak, bu kod "myVar" adında bir değişken oluşturur. Ardından kod, "myVar" a "5" atar. Şimdi, kodda "myVar" tekrar belirirse, program onu "5" gibi değerlendirecektir.
 */

// Assign the value "7" to variable "a"
// "a" değişkenine "7" değerini atayın

var a;
a = 7;